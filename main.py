class Rocket(pygame.Rect):
    """Player-controlled rocket sprite.

    Inherits from :class:`pygame.Rect` so collision detection can use
    ``pygame.Rect.colliderect`` directly.

    Parameters
    ----------
    img : pygame.Surface
        Pre-loaded rocket image surface.
    """

    __slots__ = ("img", "velocity_y")

    def __init__(self, img: pygame.Surface) -> None:
        super().__init__(GAME_WIDTH // 8, GAME_HEIGHT // 2, ROCKET_W, ROCKET_H)
        self.img: pygame.Surface = img
        self.velocity_y: float = 0.0


class Asteroid(pygame.Rect):
    """A single asteroid-column obstacle.

    Parameters
    ----------
    img    : pygame.Surface  – pre-loaded texture.
    is_top : bool            – True if this is the upper column.
    """

    __slots__ = ("img", "is_top", "passed")

    def __init__(self, img: pygame.Surface, is_top: bool) -> None:
        super().__init__(GAME_WIDTH, 0, ASTEROID_W, ASTEROID_H)
        self.img:    pygame.Surface = img
        self.is_top: bool = is_top
        self.passed: bool = False


