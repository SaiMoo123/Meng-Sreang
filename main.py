def draw_text_centered(
    surface: pygame.Surface,
    text: str,
    font: pygame.font.Font,
    color: tuple,
    y: int,
    shadow: bool = True,
) -> None:
    """Render *text* horizontally centered on *surface* at vertical position *y*.

    Parameters
    ----------
    surface : pygame.Surface
    text    : str
    font    : pygame.font.Font
    color   : tuple[int, int, int]
    y       : int   – top of the text rect
    shadow  : bool  – draw a dark offset shadow first
    """
    rendered = font.render(text, True, color)
    x = (GAME_WIDTH - rendered.get_width()) // 2
    if shadow:
        shadow_surf = font.render(text, True, (0, 0, 0))
        surface.blit(shadow_surf, (x + 2, y + 2))
    surface.blit(rendered, (x, y))


def draw_panel(surface: pygame.Surface, rect: pygame.Rect, alpha: int = 180) -> None:
    """Draw a semi-transparent dark panel for menu backgrounds.

    Parameters
    ----------
    surface : pygame.Surface
    rect    : pygame.Rect
    alpha   : int  – 0 (transparent) … 255 (opaque)
    """
    panel = pygame.Surface((rect.width, rect.height), pygame.SRCALPHA)
    panel.fill((10, 5, 30, alpha))
    # border
    pygame.draw.rect(panel, CYAN + (200,), panel.get_rect(), width=2, border_radius=12)
    surface.blit(panel, rect.topleft)


# ─────────────────────────────────────────────────────────────────────────────
# LEVEL TRANSITION SCREEN
# ─────────────────────────────────────────────────────────────────────────────

def show_level_intro(
    surface: pygame.Surface,
    bg: pygame.Surface,
    stars: list[Star],
    level: int,
    font_big: pygame.font.Font,
    font_med: pygame.font.Font,
    clock: pygame.time.Clock,
) -> None:
    """Display a brief animated countdown before gameplay starts.

    NOTE: This uses a simple frame counter rather than ``pygame.time.wait``
    so that quit events can still be processed during the transition.

    Parameters
    ----------
    surface   : pygame.Surface – main window
    bg        : pygame.Surface – background image
    stars     : list[Star]
    level     : int
    font_big  : large font
    font_med  : medium font
    clock     : pygame.time.Clock
    """
    label, *_ = LEVEL_CONFIG[level]
    frames = FPS * 2  # 2-second intro
    for frame in range(frames):
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                pygame.quit()
                sys.exit()

        surface.blit(bg, (0, 0))
        scroll_stars(stars, dt_factor=0.5)
        draw_stars(surface, stars)

        draw_text_centered(surface, f"LEVEL {level}", font_big, GOLD, 200)
        draw_text_centered(surface, label, font_med, CYAN, 262)
        countdown = 3 - frame // FPS
        if countdown > 0:
            draw_text_centered(surface, str(countdown), font_big, WHITE, 340)
        else:
            draw_text_centered(surface, "GO!", font_big, GREEN, 340)

        pygame.display.flip()
        clock.tick(FPS)


