@dataclass
class SaveData:
    """Persistent player progress.

    Attributes
    ----------
    max_unlocked_level : int
        Highest level the player has unlocked (1-10).
    last_level : int
        The level that was active when the player last quit.
    sound_on : bool
        Whether sound is currently enabled.
    high_scores : dict[str, int]
        Best score per level key (e.g. ``"level_3"``).
    """

    max_unlocked_level: int = 1
    last_level: int = 1
    sound_on: bool = True
    high_scores: dict = field(default_factory=dict)


def load_save() -> SaveData:
    """Load *SaveData* from ``save_data.json``.

    Returns a fresh :class:`SaveData` if the file is absent.

    Raises
    ------
    SaveDataError
        If the file exists but is malformed JSON.
    """
    if not SAVE_FILE.exists():
        return SaveData()

    try:
        raw = SAVE_FILE.read_text(encoding="utf-8")
        obj = json.loads(raw)
        return SaveData(
            max_unlocked_level=int(obj.get("max_unlocked_level", 1)),
            last_level=int(obj.get("last_level", 1)),
            sound_on=bool(obj.get("sound_on", True)),
            high_scores=dict(obj.get("high_scores", {})),
        )
    except (json.JSONDecodeError, KeyError, TypeError) as exc:
        raise SaveDataError(f"Cannot parse save file — {exc}") from exc


def persist_save(data: SaveData) -> None:
    """Write *data* to ``save_data.json``.

    Raises
    ------
    SaveDataError
        If the file cannot be written.
    """
    try:
        payload = {
            "max_unlocked_level": data.max_unlocked_level,
            "last_level": data.last_level,
            "sound_on": data.sound_on,
            "high_scores": data.high_scores,
        }
        SAVE_FILE.write_text(json.dumps(payload, indent=2), encoding="utf-8")
    except OSError as exc:
        raise SaveDataError(f"Cannot write save file — {exc}") from exc


