@dataclass
class Star:
    """A single parallax background star.

    Attributes
    ----------
    x, y    : float  – current position.
    speed   : float  – scroll speed (pixels per frame).
    size    : int    – drawn radius in pixels.
    bright  : int    – grey brightness 0-255.
    """

    x: float
    y: float
    speed: float
    size: int
    bright: int


def make_stars(count: int = 60) -> list[Star]:
    """Generate *count* random stars for parallax scrolling.

    NOTE: Stars are intentionally sparse so they don't obscure the
    background image that already contains stars.
    """
    return [
        Star(
            x=random.uniform(0, GAME_WIDTH),
            y=random.uniform(0, GAME_HEIGHT),
            speed=random.uniform(0.3, 1.2),
            size=random.choice([1, 1, 1, 2]),
            bright=random.randint(180, 255),
        )
        for _ in range(count)
    ]


def scroll_stars(stars: list[Star], dt_factor: float = 1.0) -> None:
    """Move each star left and wrap around the screen.

    Parameters
    ----------
    stars     : list[Star]
    dt_factor : float  – speed multiplier (scales with game velocity).
    """
    for s in stars:
        s.x -= s.speed * dt_factor
        if s.x < 0:
            s.x = GAME_WIDTH
            s.y = random.uniform(0, GAME_HEIGHT)


def draw_stars(surface: pygame.Surface, stars: list[Star]) -> None:
    """Render all stars onto *surface*."""
    for s in stars:
        col = (s.bright, s.bright, s.bright)
        if s.size == 1:
            surface.set_at((int(s.x), int(s.y)), col)
        else:
            pygame.draw.circle(surface, col, (int(s.x), int(s.y)), s.size)


