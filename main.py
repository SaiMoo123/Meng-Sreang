class GameSession:
    """Holds all mutable state for one active gameplay session.

    Parameters
    ----------
    level      : int             – starting level (1-10)
    rocket_img : pygame.Surface  – player sprite
    top_img    : pygame.Surface  – top asteroid texture
    bot_img    : pygame.Surface  – bottom asteroid texture

    Raises
    ------
    LevelRangeError
        If *level* is outside 1-10.
    """

    def __init__(
        self,
        level: int,
        rocket_img: pygame.Surface,
        top_img: pygame.Surface,
        bot_img: pygame.Surface,
    ) -> None:
        if level not in LEVEL_CONFIG:
            raise LevelRangeError(level)

        self.level:       int = level
        self.rocket_img:  pygame.Surface = rocket_img
        self.top_img:     pygame.Surface = top_img
        self.bot_img:     pygame.Surface = bot_img

        self.rocket:      Rocket = Rocket(rocket_img)
        self.asteroids:   list[Asteroid] = []
        self.score:       int = 0
        self.game_over:   bool = False
        self.level_clear: bool = False

        self._vel_x:    float = 0.0
        self._gap:      int   = 0
        self._spawn_ms: int   = 0
        self._apply_level_config()

    # ── internal helpers ─────────────────────────────────────────────────────

    def _apply_level_config(self) -> None:
        """Read speed, gap, and spawn rate from :data:`LEVEL_CONFIG`."""
        _label, vel_x, gap, spawn_ms = LEVEL_CONFIG[self.level]
        self._vel_x    = vel_x
        self._gap      = gap
        self._spawn_ms = spawn_ms

    # ── public API ───────────────────────────────────────────────────────────

    def spawn_pair(self) -> None:
        """Create one top + one bottom asteroid pair.

        NOTE: Random Y offset is clamped so the gap is always at least
        partially on screen, regardless of level config.
        """
        offset = random.randint(-ASTEROID_H // 4, ASTEROID_H // 4)

        top = Asteroid(self.top_img, is_top=True)
        top.x = GAME_WIDTH
        top.y = -ASTEROID_H + offset
        self.asteroids.append(top)

        bot = Asteroid(self.bot_img, is_top=False)
        bot.x = GAME_WIDTH
        bot.y = top.y + ASTEROID_H + self._gap
        self.asteroids.append(bot)

    def update(self) -> None:
        """Advance physics by one frame.

        Updates rocket gravity, asteroid positions, collision detection,
        and score. Sets :attr:`game_over` or :attr:`level_clear` as needed.

        NOTE: Scoring counts each *pair* as one point (each pipe in the pair
        increments by 0.5 so we use integer score after two increments).
        """
        if self.game_over or self.level_clear:
            return

        # rocket gravity
        self.rocket.velocity_y += GRAVITY
        self.rocket.y += math.floor(self.rocket.velocity_y)

        # floor / ceiling
        if self.rocket.y < 0:
            self.rocket.y = 0
            self.rocket.velocity_y = 0.0
        if self.rocket.y + ROCKET_H > GAME_HEIGHT:
            self.game_over = True
            return

        # asteroids
        for ast in self.asteroids:
            ast.x += math.floor(self._vel_x)

            # scoring
            if not ast.passed and self.rocket.x > ast.x + ASTEROID_W:
                ast.passed = True
                self.score += 1  # +1 per column → +2 per pair total

            # collision
            if self.rocket.colliderect(ast):
                self.game_over = True
                return

        # remove off-screen asteroids
        self.asteroids = [a for a in self.asteroids if a.x > -ASTEROID_W]

        # pairs scored = score // 2
        pairs = self.score // 2
        if pairs >= SCORE_TO_ADVANCE:
            self.level_clear = True

    def jump(self) -> None:
        """Apply upward velocity to the rocket (player input)."""
        self.rocket.velocity_y = JUMP_VELOCITY

    def reset(self) -> None:
        """Reset rocket and asteroids to start-of-level state."""
        self.rocket.x = GAME_WIDTH // 8
        self.rocket.y = GAME_HEIGHT // 2
        self.rocket.velocity_y = 0.0
        self.asteroids.clear()
        self.score = 0
        self.game_over = False
        self.level_clear = False

    def advance_level(self) -> bool:
        """Move to the next level if one exists.

        Returns
        -------
        bool
            True if advanced successfully, False if already at level 10.
        """
        if self.level >= 10:
            return False
        self.level += 1
        self._apply_level_config()
        self.reset()
        return True

    @property
    def pairs_scored(self) -> int:
        """Number of asteroid pairs the player has passed."""
        return self.score // 2

    @property
    def spawn_interval_ms(self) -> int:
        """Current asteroid spawn interval in milliseconds."""
        return self._spawn_ms


