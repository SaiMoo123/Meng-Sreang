class Menu:
    """Animated main-menu renderer and input handler.

    Parameters
    ----------
    surface    : pygame.Surface
    bg         : pygame.Surface  – background image
    stars      : list[Star]
    font_big   : large title font
    font_med   : menu item font
    font_small : small info font
    save       : SaveData

    Attributes
    ----------
    selection : str
        The menu item chosen by the player.  One of:
        ``"start"``, ``"continue"``, ``"level"``, ``"sound"``, ``"exit"``.
    chosen_level : int
        Level chosen when ``selection == "level"`` or ``"continue"``.
    """

    ITEMS = ["Start", "Continue", "Level", "Sound", "Exit"]

    def __init__(
        self,
        surface:    pygame.Surface,
        bg:         pygame.Surface,
        stars:      list[Star],
        font_big:   pygame.font.Font,
        font_med:   pygame.font.Font,
        font_small: pygame.font.Font,
        save:       SaveData,
    ) -> None:
        self.surface    = surface
        self.bg         = bg
        self.stars      = stars
        self.font_big   = font_big
        self.font_med   = font_med
        self.font_small = font_small
        self.save       = save

        self.cursor:       int = 0   # index into ITEMS
        self.selection:    str = ""
        self.chosen_level: int = 1

        self._anim_tick: int = 0

    # ── sub-screens ──────────────────────────────────────────────────────────

    def _level_select_screen(self, clock: pygame.time.Clock) -> Optional[int]:
        """Show the level-select sub-screen.

        Returns the chosen level (1-10) or *None* if the player pressed
        Escape to go back.
        """
        cursor = 0
        unlocked = self.save.max_unlocked_level

        while True:
            self._anim_tick += 1
            for event in pygame.event.get():
                if event.type == pygame.QUIT:
                    pygame.quit()
                    sys.exit()
                if event.type == pygame.KEYDOWN:
                    if event.key == pygame.K_ESCAPE:
                        return None
                    if event.key in (pygame.K_UP, pygame.K_w):
                        cursor = (cursor - 1) % 10
                    if event.key in (pygame.K_DOWN, pygame.K_s):
                        cursor = (cursor + 1) % 10
                    if event.key in (pygame.K_RETURN, pygame.K_SPACE):
                        chosen = cursor + 1
                        if chosen <= unlocked:
                            return chosen

            self.surface.blit(self.bg, (0, 0))
            scroll_stars(self.stars)
            draw_stars(self.surface, self.stars)

            draw_panel(self.surface, pygame.Rect(30, 30, 300, 580))
            draw_text_centered(self.surface, "SELECT LEVEL", self.font_big, CYAN, 45)

            for i in range(10):
                lvl = i + 1
                label, *_ = LEVEL_CONFIG[lvl]
                locked = lvl > unlocked
                y = 120 + i * 44
                if i == cursor:
                    glow = abs(math.sin(self._anim_tick * 0.06)) * 40
                    col = (int(200 + glow), int(220 + glow // 2), 255)
                    draw_text_centered(self.surface, f"► Level {lvl}  [{label}]", self.font_med, col, y)
                elif locked:
                    draw_text_centered(self.surface, f"  Level {lvl}  🔒", self.font_small, GREY, y + 8)
                else:
                    draw_text_centered(self.surface, f"  Level {lvl}  [{label}]", self.font_med, WHITE, y)

            draw_text_centered(self.surface, "ESC = Back", self.font_small, GREY, 570)
            pygame.display.flip()
            clock.tick(FPS)

    # ── main loop ────────────────────────────────────────────────────────────

    def run(self, clock: pygame.time.Clock) -> None:
        """Block until the player picks a menu action.

        Results are stored in :attr:`selection` and :attr:`chosen_level`.
        """
        self.selection = ""
        self.cursor = 0

        while not self.selection:
            self._anim_tick += 1

            for event in pygame.event.get():
                if event.type == pygame.QUIT:
                    self.selection = "exit"

                if event.type == pygame.KEYDOWN:
                    if event.key in (pygame.K_UP, pygame.K_w):
                        self.cursor = (self.cursor - 1) % len(self.ITEMS)
                    elif event.key in (pygame.K_DOWN, pygame.K_s):
                        self.cursor = (self.cursor + 1) % len(self.ITEMS)
                    elif event.key in (pygame.K_RETURN, pygame.K_SPACE):
                        self._activate(clock)

            self._draw()
            pygame.display.flip()
            clock.tick(FPS)

    def _activate(self, clock: pygame.time.Clock) -> None:
        """Handle the currently highlighted menu item."""
        item = self.ITEMS[self.cursor].lower()

        if item == "start":
            self.chosen_level = 1
            self.selection = "start"

        elif item == "continue":
            self.chosen_level = self.save.last_level
            self.selection = "continue"

        elif item == "level":
            chosen = self._level_select_screen(clock)
            if chosen is not None:
                self.chosen_level = chosen
                self.selection = "level"

        elif item == "sound":
            self.save.sound_on = not self.save.sound_on
            try:
                persist_save(self.save)
            except SaveDataError:
                pass  # non-critical — ignore silently

        elif item == "exit":
            self.selection = "exit"

    def _draw(self) -> None:
        """Render one frame of the main menu."""
        self.surface.blit(self.bg, (0, 0))
        scroll_stars(self.stars)
        draw_stars(self.surface, self.stars)

        # title panel
        draw_panel(self.surface, pygame.Rect(20, 40, 320, 120), alpha=200)
        draw_text_centered(self.surface, "SPACE FLAPPY", self.font_big, GOLD, 60)
        draw_text_centered(self.surface, "🚀  Dodge the Asteroids!", self.font_small, CYAN, 118)

        # menu items panel
        draw_panel(self.surface, pygame.Rect(60, 200, 240, 320), alpha=190)

        for i, item in enumerate(self.ITEMS):
            y = 220 + i * 58
            if i == self.cursor:
                glow = abs(math.sin(self._anim_tick * 0.07)) * 50
                col = (int(150 + glow), int(210 + glow // 2), 255)
                label = f"► {item}"
                if item == "Sound":
                    label += "  [ON]" if self.save.sound_on else "  [OFF]"
                draw_text_centered(self.surface, label, self.font_med, col, y)
            else:
                col = WHITE
                label = item
                if item == "Sound":
                    label += "  [ON]" if self.save.sound_on else "  [OFF]"
                draw_text_centered(self.surface, label, self.font_med, col, y)

        # footer
        draw_text_centered(self.surface, "↑↓ Navigate   Enter Select", self.font_small, GREY, 545)
        unlocked = self.save.max_unlocked_level
        draw_text_centered(
            self.surface,
            f"Highest level unlocked: {unlocked}",
            self.font_small,
            GREY,
            572,
        )


# ─────────────────────────────────────────────────────────────────────────────
# HUD
# ─────────────────────────────────────────────────────────────────────────────

def draw_hud(
    surface:    pygame.Surface,
    session:    GameSession,
    font_med:   pygame.font.Font,
    font_small: pygame.font.Font,
) -> None:
    """Render the in-game heads-up display.

    Shows current level, pairs scored, and target score.

    Parameters
    ----------
    surface    : pygame.Surface
    session    : GameSession
    font_med   : medium font
    font_small : small font
    """
    label, *_ = LEVEL_CONFIG[session.level]
    pairs = session.pairs_scored

    # score
    draw_text_centered(surface, str(pairs), font_med, WHITE, 10, shadow=True)

    # level badge (top-left)
    badge = font_small.render(f"Lv {session.level}  {label}", True, CYAN)
    surface.blit(badge, (8, 12))

    # target (top-right)
    target_txt = font_small.render(f"Goal: {SCORE_TO_ADVANCE}", True, GOLD)
    surface.blit(target_txt, (GAME_WIDTH - target_txt.get_width() - 8, 12))


# ─────────────────────────────────────────────────────────────────────────────
# GAME-OVER / LEVEL-CLEAR OVERLAYS
# ─────────────────────────────────────────────────────────────────────────────

def draw_game_over(
    surface:    pygame.Surface,
    session:    GameSession,
    font_big:   pygame.font.Font,
    font_med:   pygame.font.Font,
    font_small: pygame.font.Font,
    save:       SaveData,
) -> None:
    """Render the game-over overlay.

    Shows score, high-score for this level, and restart / menu hint.

    Parameters
    ----------
    surface    : pygame.Surface
    session    : GameSession
    font_big   : title font
    font_med   : score font
    font_small : hint font
    save       : SaveData  – used to display high-score
    """
    draw_panel(surface, pygame.Rect(40, 180, 280, 280), alpha=220)
    draw_text_centered(surface, "GAME OVER", font_big, RED, 200)

    pairs = session.pairs_scored
    draw_text_centered(surface, f"Score: {pairs}", font_med, WHITE, 265)

    key = f"level_{session.level}"
    best = save.high_scores.get(key, 0)
    draw_text_centered(surface, f"Best: {best}", font_med, GOLD, 308)

    draw_text_centered(surface, "Space / Click = Retry", font_small, CYAN, 365)
    draw_text_centered(surface, "Esc = Menu",            font_small, GREY, 390)


def draw_level_clear(
    surface:    pygame.Surface,
    session:    GameSession,
    font_big:   pygame.font.Font,
    font_med:   pygame.font.Font,
    font_small: pygame.font.Font,
    tick:       int,
) -> None:
    """Render the level-clear celebration overlay.

    Parameters
    ----------
    tick : int  – frame counter for animation.
    """
    draw_panel(surface, pygame.Rect(40, 160, 280, 320), alpha=220)
    draw_text_centered(surface, "LEVEL CLEAR!", font_big, GREEN, 180)

    glow_val = int(abs(math.sin(tick * 0.08)) * 80 + 160)
    star_col = (glow_val, glow_val, 80)
    draw_text_centered(surface, "★★★", font_big, star_col, 240)

    draw_text_centered(surface, f"Score: {session.pairs_scored}", font_med, WHITE, 308)

    if session.level < 10:
        draw_text_centered(surface, "Space = Next Level", font_small, CYAN, 375)
    else:
        draw_text_centered(surface, "🏆  YOU WIN — ALL DONE!", font_med, GOLD, 375)

    draw_text_centered(surface, "Esc = Menu", font_small, GREY, 410)


# ─────────────────────────────────────────────────────────────────────────────
# MAIN APPLICATION
# ─────────────────────────────────────────────────────────────────────────────

def main() -> None:
    """Entry point — initialises pygame, loads assets, and runs the game loop.

    Flow
    ----
    1. Load save data (gracefully falls back on corrupt file).
    2. Load images; abort with a clear message if any are missing.
    3. Show main menu.
    4. Run gameplay session.
    5. On game-over or level-clear, update save and return to menu.

    NOTE: The outer try/finally ensures ``pygame.quit()`` is called even if
    an unexpected exception propagates out of the game loop.
    """
    # ── pygame init ──────────────────────────────────────────────────────────
    pygame.init()
    window = pygame.display.set_mode((GAME_WIDTH, GAME_HEIGHT))
    pygame.display.set_caption("Space Flappy 🚀")

    clock = pygame.time.Clock()

    # ── load save ────────────────────────────────────────────────────────────
    try:
        save = load_save()
    except SaveDataError as exc:
        print(f"[WARNING] {exc}  — starting fresh.")
        save = SaveData()

    # ── load assets ──────────────────────────────────────────────────────────
    try:
        # icon
        icon_path = IMAGES_DIR / "icon.png"
        if icon_path.exists():
            pygame.display.set_icon(pygame.image.load(str(icon_path)))

        bg_surf    = load_image("background.png",   (GAME_WIDTH, GAME_HEIGHT), alpha=False)
        rocket_img = load_image("rocket.png",       (ROCKET_W, ROCKET_H))
        top_img    = load_image("asteroid_top.png", (ASTEROID_W, ASTEROID_H))
        bot_img    = load_image("asteroid_bot.png", (ASTEROID_W, ASTEROID_H))

    except AssetLoadError as exc:
        print(f"[ERROR] {exc}")
        print("Run generate_images.py first to create the required images.")
        pygame.quit()
        sys.exit(1)

    # ── fonts ────────────────────────────────────────────────────────────────
    try:
        font_big   = pygame.font.SysFont("Arial", 38, bold=True)
        font_med   = pygame.font.SysFont("Arial", 26)
        font_small = pygame.font.SysFont("Arial", 18)
    except Exception:
        font_big   = pygame.font.Font(None, 38)
        font_med   = pygame.font.Font(None, 26)
        font_small = pygame.font.Font(None, 18)

    # ── stars ────────────────────────────────────────────────────────────────
    stars = make_stars(60)

    # ── custom events ────────────────────────────────────────────────────────
    SPAWN_ASTEROIDS = pygame.USEREVENT + 1

    # ── session state ────────────────────────────────────────────────────────
    session: Optional[GameSession] = None
    in_game = False
    anim_tick = 0

    menu = Menu(window, bg_surf, stars, font_big, font_med, font_small, save)

    # ── main application loop ────────────────────────────────────────────────
    try:
        while True:
            # ── MENU ─────────────────────────────────────────────────────────
            menu.run(clock)

            if menu.selection == "exit":
                break

            start_level = menu.chosen_level

            # ── start gameplay session ────────────────────────────────────────
            try:
                session = GameSession(start_level, rocket_img, top_img, bot_img)
            except LevelRangeError as exc:
                print(f"[ERROR] {exc}")
                continue

            # arm spawn timer
            pygame.time.set_timer(SPAWN_ASTEROIDS, session.spawn_interval_ms)

            show_level_intro(window, bg_surf, stars, session.level,
                             font_big, font_med, clock)

            in_game = True
            save.last_level = session.level

            # ── gameplay loop ─────────────────────────────────────────────────
            while in_game:
                anim_tick += 1

                # events
                for event in pygame.event.get():
                    if event.type == pygame.QUIT:
                        in_game = False
                        menu.selection = "exit"

                    elif event.type == SPAWN_ASTEROIDS:
                        if not session.game_over and not session.level_clear:
                            session.spawn_pair()

                    elif event.type == pygame.KEYDOWN:
                        if event.key == pygame.K_ESCAPE:
                            in_game = False  # back to menu

                        elif event.key in (pygame.K_SPACE, pygame.K_UP, pygame.K_w):
                            if session.game_over:
                                # update high-score before reset
                                key = f"level_{session.level}"
                                best = save.high_scores.get(key, 0)
                                if session.pairs_scored > best:
                                    save.high_scores[key] = session.pairs_scored
                                session.reset()
                                pygame.time.set_timer(SPAWN_ASTEROIDS, session.spawn_interval_ms)

                            elif session.level_clear:
                                if not session.advance_level():
                                    in_game = False  # won the whole game
                                else:
                                    pygame.time.set_timer(SPAWN_ASTEROIDS, session.spawn_interval_ms)
                                    # update unlocked level
                                    if session.level > save.max_unlocked_level:
                                        save.max_unlocked_level = session.level
                                    show_level_intro(window, bg_surf, stars,
                                                     session.level, font_big, font_med, clock)
                            else:
                                session.jump()

                    elif event.type == pygame.MOUSEBUTTONDOWN:
                        if event.button == 1:
                            if not session.game_over and not session.level_clear:
                                session.jump()
                            elif session.game_over:
                                key = f"level_{session.level}"
                                best = save.high_scores.get(key, 0)
                                if session.pairs_scored > best:
                                    save.high_scores[key] = session.pairs_scored
                                session.reset()
                                pygame.time.set_timer(SPAWN_ASTEROIDS, session.spawn_interval_ms)

                # ── update ───────────────────────────────────────────────────
                if not session.game_over and not session.level_clear:
                    session.update()

                    # update unlocked level on clear
                    if session.level_clear:
                        next_lv = session.level  # still current (before advance)
                        if next_lv > save.max_unlocked_level:
                            save.max_unlocked_level = next_lv
                        key = f"level_{next_lv}"
                        best = save.high_scores.get(key, 0)
                        if session.pairs_scored > best:
                            save.high_scores[key] = session.pairs_scored
                        save.last_level = next_lv
                        try:
                            persist_save(save)
                        except SaveDataError as exc:
                            print(f"[WARNING] Could not save progress — {exc}")

                # ── draw ─────────────────────────────────────────────────────
                window.blit(bg_surf, (0, 0))
                scroll_stars(stars, dt_factor=abs(session._vel_x) / 3)
                draw_stars(window, stars)

                for ast in session.asteroids:
                    window.blit(ast.img, ast)

                window.blit(session.rocket.img, session.rocket)

                draw_hud(window, session, font_med, font_small)

                if session.game_over:
                    draw_game_over(window, session, font_big, font_med, font_small, save)

                elif session.level_clear:
                    draw_level_clear(window, session, font_big, font_med, font_small, anim_tick)

                pygame.display.flip()
                clock.tick(FPS)

            if menu.selection == "exit":
                break

    finally:
        # NOTE: pygame.quit() is called here regardless of how we exit so
        # that the display is properly torn down even on unexpected errors.
        try:
            persist_save(save)
        except SaveDataError as exc:
            print(f"[WARNING] Final save failed — {exc}")
        pygame.quit()


# ─────────────────────────────────────────────────────────────────────────────
if __name__ == "__main__":
    main()
