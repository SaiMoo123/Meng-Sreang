def load_image(filename: str, size: tuple[int, int], alpha: bool = True) -> pygame.Surface:
    """Load and scale an image from the ``images/`` directory.

    Parameters
    ----------
    filename : str
        File name inside ``images/``.
    size : tuple[int, int]
        Target (width, height) after scaling.
    alpha : bool
        If True use ``convert_alpha()``, else ``convert()``.

    Returns
    -------
    pygame.Surface

    Raises
    ------
    AssetLoadError
        If the file does not exist or pygame fails to load it.
    """
    path = IMAGES_DIR / filename
    if not path.exists():
        raise AssetLoadError(str(path))
    try:
        surf = pygame.image.load(str(path))
        surf = surf.convert_alpha() if alpha else surf.convert()
        return pygame.transform.scale(surf, size)
    except pygame.error as exc:
        raise AssetLoadError(str(path)) from exc


