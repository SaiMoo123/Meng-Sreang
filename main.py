class GameError(Exception):
    """Base exception for all Space Flappy errors."""


class AssetLoadError(GameError):
    """Raised when a required image or sound file cannot be loaded.

    Argument
    --------
    path : str
        Filesystem path that failed to load.
    """

    def __init__(self, path: str) -> None:
        self.path = path
        super().__init__(f"Failed to load asset: '{path}'")


class SaveDataError(GameError):
    """Raised when the save file cannot be read or written.

    Argument
    --------
    detail : str
        Human-readable description of what went wrong.
    """

    def __init__(self, detail: str) -> None:
        super().__init__(f"Save-data error — {detail}")


class LevelRangeError(GameError, ValueError):
    """Raised when a level number is outside the valid 1-10 range.

    Argument
    --------
    level : int
        The out-of-range level that was requested.
    """

    def __init__(self, level: int) -> None:
        self.level = level
        super().__init__(f"Level {level} is out of range (must be 1-10)")


