"""
Space Flappy — main.py
======================
A space-themed Flappy-Bird-style game built with pygame.

Character : a rocket ship (images/rocket.png)
Background: deep-space starfield with nebula (images/background.png)
Obstacles : asteroid columns (images/asteroid_top.png / asteroid_bot.png)

Menu items
----------
  Start    – begin a new game from Level 1
  Continue – resume from the last completed level (saved in save_data.json)
  Level    – choose any previously unlocked level
  Sound    – toggle sound effects / music on or off
  Exit     – quit the application

Levels (10 total)
-----------------
  1-3  Easy   – slower speed, wider gap, fewer asteroids
  4-6  Medium – moderate speed and gap
  7-10 Hard   – fast speed, narrow gap, rapid spawn

Python features used
--------------------
  • Docstrings  (module, class, function)
  • Type annotations
  • Custom exceptions (GameError, AssetLoadError, SaveDataError)
  • try / except / finally blocks throughout
  • Dataclass-style __slots__ on game objects
  • Module-level NOTE markers for important design decisions
"""

